public class DordIslem {
    public double Topla(double num1, double num2) {
        return num1 + num2;
    }
    public double  Cix(double num1,double num2){
        return num1+num2;
    }
    public double  Bol(double num1,double num2){
        return Math.max(num1,num2)/Math.min(num1,num2);
    }
    public double  Vur(double num1,double num2){
        return num1*num2;
    }
}
